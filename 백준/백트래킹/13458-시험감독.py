# 📌 문제 목표
# 모든 시험장 학생을 감시하는데 필요한 최소 감독관 수
# 💡 해법 요약

# 각 시험장마다 총감독관 1명 필수 배치
# 남은 학생 수를 부감독관 능력으로 나눈 값 올림
# 모든 시험장의 감독관 수 합산

# 🏫 시험장이 몇 개인지 입력받기
n = int(input())

# 👥 각 시험장에 학생이 몇 명인지 입력받기
# 예: [10, 9, 10, 9, 9] → 5개 시험장에 각각 10,9,10,9,9명
students = list(map(int, input().split()))

# 👮 감독관이 감시할 수 있는 학생 수
# b: 총감독관 한 명이 볼 수 있는 학생 수 (예: 5명)
# c: 부감독관 한 명이 볼 수 있는 학생 수 (예: 2명)
b, c = map(int, input().split())

# 📊 필요한 감독관 총 인원 세기
total = 0

# 🔍 각 시험장을 하나씩 확인해요
for student in students:
    # 1️⃣ 총감독관 1명은 꼭 필요해요! (규칙)
    total += 1  # 감독관 +1
    
    # 2️⃣ 총감독관이 보고 남은 학생 계산
    # 예: 10명 - 5명(총감독) = 5명 남음
    remaining = student - b
    
    # 3️⃣ 남은 학생이 있으면 부감독관 추가
    if remaining > 0:  # 아직 감시 안 된 학생이 있나요?
        # 부감독관 몇 명 필요한지 계산 (올림!)
        # 예: 5명 남고, 부감독 1명이 2명 볼 수 있으면
        #     5 ÷ 2 = 2.5 → 올림해서 3명 필요!
        need = (remaining + c - 1) // c  # 올림 공식
        total += need  # 부감독관 추가

# 💯 총 몇 명의 감독관이 필요한지 출력!
print(total)